using System;
using System.Collections.Generic;
using System.Threading;

public class ReflectingActivity : Activity
{
    private List<string> _prompts;
    private List<string> _questions;

    public ReflectingActivity()
        : base("Reflecting", "This activity will help you reflect on times in your life when you have shown strength and resilience. It will help you recognize the power you have and how you can use it in other situations.")
    {
        _prompts = new List<string>()
        {
            "Think of a time when you stood up for someone else.",
            "Think of a time when you did something really difficult.",
            "Think of a time when you helped someone in need.",
            "Think of a time when you did something truly selfless."
        };

        _questions = new List<string>()
        {
            "Why was this experience meaningful to you?",
            "Have you ever done anything like this before?",
            "How did you get started?",
            "How did you feel when it was complete?",
            "What made this time different than other times when you were not as successful?",
            "What is your favorite thing about this experience?",
            "What could you learn from this experience that applies to other situations?",
            "How can you keep this experience in mind in the future?"
        };
    }

    public override void Run()
    {
        StartingMessage();
        Console.WriteLine(); 
        Console.WriteLine("Consider the following prompt:");
        Console.WriteLine();
        Random random = new Random();
        string prompt = _prompts[random.Next(_prompts.Count)];
        Console.WriteLine($" {prompt} ");
        Console.WriteLine();
        Console.Write("When you have something in mind, press Enter to continue. ");
        Console.ReadLine();
        Console.WriteLine("\nNow ponder on each of the following questions as they relate to this experience...");
        Console.Write("\nYou may begin in: ");
        ShowCountdown(5);
        Console.Clear();

        // Show random questions until time runs out
        int elapsed = 0;
        while (elapsed < _duration)
        {
            string question = _questions[random.Next(_questions.Count)];
            Console.Write($"> {question} ");
            ShowSpinner(4); // wait a few seconds to ponder

            elapsed += 5; // roughly estimate 5 seconds per question
            Console.WriteLine();
        }

        EndingMessage();
    }
}
